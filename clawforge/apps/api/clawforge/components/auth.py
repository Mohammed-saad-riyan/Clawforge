def _auth_wrapper() -> Component:
    """Wrapper widget that redirects based on auth state."""
    return Component(
        id="auth_wrapper",
        name="Auth Wrapper",
        description="Widget that shows login or main app based on auth state",
        category=ComponentCategory.AUTH,
        tags=["auth", "wrapper", "navigation", "redirect"],
        requires=["auth_provider"],
        dependencies={
            "flutter_riverpod": "^2.5.1",
        },
        files=[
            {
                "path": "lib/features/auth/presentation/widgets/auth_wrapper.dart",
                "content": '''import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import '../providers/auth_provider.dart';

class AuthWrapper extends ConsumerWidget {
  final Widget authenticatedChild;
  final Widget unauthenticatedChild;

  const AuthWrapper({
    super.key,
    required this.authenticatedChild,
    required this.unauthenticatedChild,
  });

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final authState = ref.watch(authProvider);

    if (authState.isLoading) {
      return const Scaffold(
        body: Center(
          child: CircularProgressIndicator(),
        ),
      );
    }

    return authState.isAuthenticated
        ? authenticatedChild
        : unauthenticatedChild;
  }
}
''',
            },
        ],
    )
